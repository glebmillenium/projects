    public class Panel extends JPanel{
        private Tuple[] first; 
        private Tuple[] second;
        int sign = 0;
        
        Panel(){
        
        }
        
        public void paintComponent(Graphics g){
            super.paintComponent(g);
            if(sign != 0){
                painter(g);
            }
        }
        
        private void painter(Graphics g)
        {
            g.setColor(Color.RED);
            for(int i = 0; i< first.length; i++){
                g.drawOval(first[i].getFirst(), 500-first[i].getSecond(), 8, 8);
            }
            g.setColor(Color.BLUE);
            for(int i = 0; i< second.length; i++){
                g.drawOval(second[i].getFirst(), 500-second[i].getSecond(), 8, 8);
            }
        }
        
        public void settingSigns(Tuple[] one, Tuple[] two){
            this.sign = 1;
            this.first = one;
            this.second = two;
            repaint();
        }
    }
    
    public class Tuple{
        private int x;
        private int y;
        Tuple(){
            this.x = 0;
            this.y = 0;
        }
        Tuple(int x, int y){
            this.x = x;
            this.y = y;
        }
        Tuple(double x, double y){
            this.x = (int) Math.round(x);
            this.y = (int) Math.round(y);
        }
        public int getFirst(){
            return this.x;
        }
        public int getSecond(){
            return this.y;
        }
    }
    
